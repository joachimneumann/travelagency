function interpolate(template, vars) {
  if (!vars || typeof vars !== "object") return template;
  return template.replace(/\{([^{}]+)\}/g, (match, key) => {
    const normalizedKey = String(key || "").trim();
    return normalizedKey in vars ? String(vars[normalizedKey]) : match;
  });
}

export function normalizePdfLang(value) {
  const normalized = normalizeLanguageCode(normalizeText(value), { fallback: DEFAULT_LANG });
  return SUPPORTED_PDF_LANGS.has(normalized) ? normalized : DEFAULT_LANG;
}

export function pdfLocale(lang) {
  return pdfLocaleForLanguage(normalizePdfLang(lang), pdfLocaleForLanguage(DEFAULT_LANG, "en-GB"));
}

export function isPdfRtl(lang) {
  return RTL_PDF_LANGS.has(normalizePdfLang(lang));
}

export function pdfTextAlign(lang, fallback = "left") {
  if (fallback === "center") return "center";
  return isPdfRtl(lang) ? "right" : fallback;
}

export function pdfTextOptions(lang, options = {}, fallbackAlign = "left") {
  return {
    ...options,
    align: options?.align || pdfTextAlign(lang, fallbackAlign)
  };
}

export function pdfT(lang, key, fallback, vars) {
  const normalizedLang = normalizePdfLang(lang);
  const template = String(DICTIONARY[normalizedLang]?.[key] ?? DICTIONARY[DEFAULT_LANG]?.[key] ?? fallback ?? key);
  return interpolate(template, vars);
}

export function formatPdfDateTime(value, lang, options = {}, fallback = "") {
  const candidate = normalizeText(value);
  if (!candidate) return fallback;
  const date = new Date(candidate);
  if (Number.isNaN(date.getTime())) return candidate;
  return new Intl.DateTimeFormat(pdfLocale(lang), options).format(date);
}

export function formatPdfDateOnly(value, lang, options = {}, fallback = "") {
  const candidate = normalizeText(value);
  if (!candidate) return fallback;
  const match = candidate.match(/^(\d{4})-(\d{2})-(\d{2})$/);
  if (!match) return candidate;
  const [, year, month, day] = match;
  const date = new Date(Date.UTC(Number(year), Number(month) - 1, Number(day)));
  if (Number.isNaN(date.getTime())) return candidate;
  return new Intl.DateTimeFormat(pdfLocale(lang), { timeZone: "UTC", ...options }).format(date);
}

export function formatPdfMoney(amountCents, currency, lang) {
  const code = normalizeText(currency) || "USD";
  const decimalPlaces = code === "VND" || code === "THB" ? 0 : 2;
  const symbolMap = {
    USD: "$",
    EUR: "€",
    EURO: "€",
    // The dong sign is not rendered reliably in every PDF font stack we ship.
    // Use the currency code for PDFs so the output stays legible everywhere.
    VND: "VND ",
    THB: "฿",
    AUD: "A$",
    GBP: "£",
    NZD: "NZ$",
    ZAR: "R"
  };
  const amount = Number(amountCents || 0) / 10 ** decimalPlaces;
  const number = new Intl.NumberFormat(pdfLocale(lang), {
    minimumFractionDigits: decimalPlaces,
    maximumFractionDigits: decimalPlaces,
    useGrouping: true
  })
    .format(amount)
    .replace(/[\u00A0\u202F]/g, " ");
  return `${symbolMap[code] || `${code} `}${number}`;
}
