#!/usr/bin/env node

import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT_DIR = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..", "..");

const CRITICAL_SOURCES = [
  "shared/css/tokens.css",
  "shared/css/base.css",
  "shared/css/utilities.css",
  "shared/css/components/backend-login.css",
  "shared/css/site-home-critical.css",
  "shared/css/components/tour-card.css",
  "shared/css/pages/home-critical.css"
];

const OUTPUTS = [
  "shared/css/home-critical-mobile.css",
  "shared/css/home-critical-desktop.css"
];

function normalizeCssForBundle(source) {
  return source.trimEnd();
}

async function buildBundle() {
  const parts = [];
  for (const relativePath of CRITICAL_SOURCES) {
    const absolutePath = path.join(ROOT_DIR, relativePath);
    const source = await readFile(absolutePath, "utf8");
    parts.push(`/* ${relativePath} */\n${normalizeCssForBundle(source)}`);
  }
  return `/* Generated by scripts/assets/build_homepage_critical_css.mjs. Do not edit by hand. */\n${parts.join("\n\n")}\n`;
}

const bundle = await buildBundle();
await Promise.all(
  OUTPUTS.map((relativePath) => writeFile(path.join(ROOT_DIR, relativePath), bundle, "utf8"))
);

console.log(`Built ${OUTPUTS.length} homepage critical CSS bundles.`);
